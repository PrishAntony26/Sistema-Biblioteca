    private void btnSolicitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSolicitarActionPerformed
        // TODO add your handling code here:
        
        if(!txtCodigo1.getText().isEmpty()){
            //Accion solicitar
            String codLib = txtCodigo1.getText();
            //Buscamos libro
            
            for(int i=0;i<Biblioteca.libros.size();i++){
                String codPrueba = Biblioteca.libros.get(i).getCodigoL();
                if(codLib.equals(codPrueba)){
                    OperacionesAlumno.PrestamoNuevoLibro(al, Biblioteca.libros.get(i));
                    ver();
                }
            }
            //Limpiar
            txtCodigo1.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese el codigo del libro!");
        }
    }//GEN-LAST:event_btnSolicitarActionPerformed

